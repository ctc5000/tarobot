    interpret(numbers) {
        const talentNum = this.calculateTalentNumber(numbers);
        const potentialNum = this.calculatePotentialNumber(numbers);

        const base = this.descriptions[talentNum] || this.getDefaultDescription(talentNum);

        return {
            talentNumber: talentNum,
            potential: potentialNum,
            title: base.title,
            description: base.description,
            detailedDescription: base.detailedDescription || base.description,
            talents: base.talents || [],
            howToDevelop: base.howToDevelop || [],
            suitable: base.suitable || [],
            combinations: base.combinations || '',
            environment: base.environment || '',
            developmentPath: base.developmentPath || base.advice || '',
            successFactors: base.successFactors || [],
            failureFactors: base.failureFactors || [],
            potentialDescription: this.getPotentialDescription(potentialNum),
            advice: base.advice || '',
            fullInterpretation: this.buildInterpretation(talentNum, potentialNum, base)
        };
    }

    calculateTalentNumber(numbers) {
        let sum = numbers.name * 2;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculatePotentialNumber(numbers) {
        const talent = this.calculateTalentNumber(numbers);
        return Math.min(100, talent * 11);
    }

    getPotentialDescription(percent) {
        if (percent >= 90) return '✨ Исключительный потенциал — вы способны на великие дела';
        if (percent >= 80) return '🌟 Выдающийся потенциал — ваши таланты ждут раскрытия';
        if (percent >= 70) return '💫 Большой потенциал — вы многого можете достичь';
        if (percent >= 60) return '⭐ Хороший потенциал — развивайте свои сильные стороны';
        if (percent >= 50) return '📈 Средний потенциал — работайте над раскрытием';
        return '🌱 Потенциал требует развития и терпения';
    }

