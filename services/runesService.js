    getRandomRunes(count) {
        const runeKeys = Object.keys(this.runes);
        const shuffled = [...runeKeys].sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    }

    getBirthRune(birthDate) {
        const day = parseInt(birthDate.split('.')[0]);
        const month = parseInt(birthDate.split('.')[1]);
        const year = parseInt(birthDate.split('.')[2]);

        // Простой алгоритм: сумма дня и месяца, деленная на 24
        const sum = day + month + (year % 24);
        const runeNumber = (sum % 24) + 1;

        return this.getRuneByNumber(runeNumber);
    }

