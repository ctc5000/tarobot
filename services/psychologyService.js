    generatePortrait(numerology, zodiac, fengShui, tarot) {
        // Проверяем наличие всех необходимых данных
        if (!numerology || !numerology.base) {
            return this.getEmptyPortrait();
        }

        const { base, achilles, control, calls } = numerology;

        // Определяем ведущую модальность (НЛП)
        const modality = this.determineModality(base.fate, base.name);

        // Определяем архетип
        const archetype = this.getArchetype(control.number || base.fate);

        // Определяем тип привязанности
        const attachment = this.determineAttachmentStyle(base);

        // Генерируем глубокий психологический портрет
        const portrait = this.generateDeepPortrait(
            numerology, zodiac, fengShui, tarot,
            modality, archetype, attachment
        );

        return {
            modality,
            archetype,
            attachment,
            portrait
        };
    }

