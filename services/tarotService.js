    calculate(numerology) {
        // Проверяем, что numerology и numerology.base существуют
        if (!numerology || !numerology.base) {
            console.error('Ошибка: numerology или numerology.base не определены');
            return {
                fate: { number: 0, name: 'Не определено', keywords: '', description: '', advice: '' },
                personality: { number: 0, name: 'Не определено', keywords: '', description: '', advice: '' },
                control: { number: 0, name: 'Не определено', keywords: '', description: '', advice: '' }
            };
        }

        const fateNumber = numerology.base.fate || 0;
        const personalityNumber = numerology.base.name || 0;

        // Получаем число управления (оно может быть в разных местах)
        let controlNumber = 0;
        if (numerology.control && numerology.control.number) {
            controlNumber = numerology.control.number;
        } else if (numerology.control && typeof numerology.control === 'number') {
            controlNumber = numerology.control;
        } else {
            controlNumber = numerology.base.control || 0;
        }

        return {
            fate: {
                number: fateNumber,
                ...this.getArcanaData(fateNumber)
            },
            personality: {
                number: personalityNumber,
                ...this.getArcanaData(personalityNumber)
            },
            control: {
                number: controlNumber,
                ...this.getArcanaData(controlNumber)
            }
        };
    }

    getArcanaData(num) {
        // Приводим число к диапазону 1-22
        let arcanaNum = num;
        if (arcanaNum > 22) {
            arcanaNum = this.reduceToRange(arcanaNum);
        }
        if (arcanaNum < 1) arcanaNum = 1;

        return this.majorArcana[arcanaNum] || this.getDefaultArcana(arcanaNum);
    }

    reduceToRange(num) {
        while (num > 22) {
            num = String(num).split('').reduce((sum, digit) => sum + parseInt(digit), 0);
        }
        return num;
    }

    getDefaultArcana(num) {
        return {
            name: `Аркан ${num}`,
            keywords: 'Тайна, ожидающая раскрытия',
            description: `Число ${num} несет в себе уникальную энергию, которую предстоит расшифровать лично вам. Это ваша индивидуальная карта, история, которую только вы можете написать.`,
            advice: `Исследуйте значение числа ${num} в своей жизни. Какие события, люди, ситуации связаны с ним? Там скрыт ответ.`,
            shadow: `Тень этого аркана проявится, если вы будете избегать его уроков.`
        };
    }
