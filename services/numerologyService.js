    reduceToRange(num, min = 1, max = 22) {
        while (num > max) {
            num = String(num).split('').reduce((sum, digit) => sum + parseInt(digit), 0);
        }
        return num;
    }

    calculateWordValue(word) {
        const cleaned = word.toLowerCase().replace(/[^а-яё]/g, '');
        let sum = 0;
        for (let char of cleaned) {
            sum += this.letterMap[char] || 0;
        }
        return this.reduceToRange(sum);
    }

    calculate(surname, firstName, patronymic, birthDate) {
        // 1. Базовые числа
        const fateNumber = this.reduceToRange(
            birthDate.split('.').join('').split('').reduce((s, d) => s + parseInt(d), 0)
        );
        const surnameNumber = this.calculateWordValue(surname);
        const nameNumber = this.calculateWordValue(firstName);
        const patronymicNumber = this.calculateWordValue(patronymic);

        const baseNumbers = [fateNumber, nameNumber, surnameNumber, patronymicNumber];

        // 2. Ахиллесова пята
        const achilles = Math.max(...baseNumbers) - Math.min(...baseNumbers);

        // 3. Число управления
        const controlNumber = this.reduceToRange(baseNumbers.reduce((s, n) => s + n, 0));

        // 4. Оклики
        const callClose = this.reduceToRange(fateNumber + nameNumber);
        const callSocial = this.reduceToRange(surnameNumber + patronymicNumber);
        const callWorld = this.reduceToRange(fateNumber + patronymicNumber);

        // 5. КАРТЫ ТАРО - теперь рассчитываются на сервере!
        const tarotCards = this.tarotService.calculateFromNumbers({
            fate: fateNumber,
            name: nameNumber,
            surname: surnameNumber,
            patronymic: patronymicNumber
        });

        return {
            base: {
                fate: fateNumber,
                name: nameNumber,
                surname: surnameNumber,
                patronymic: patronymicNumber
            },
            achilles: {
                number: achilles,
                description: this.achillesDescriptions[achilles] || this.getDefaultAchillesDescription(achilles)
            },
            control: {
                number: controlNumber,
                description: this.controlDescriptions[controlNumber] || this.getDefaultControlDescription(controlNumber)
            },
            calls: {
                close: callClose,
                social: callSocial,
                world: callWorld
            },
            tarot: tarotCards // ← ВОТ ТУТ ДОБАВЛЯЕМ КАРТЫ В ОТВЕТ
        };
    }

