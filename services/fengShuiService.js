    calculate(birthDate) {
        const year = parseInt(birthDate.split('.')[2]);
        const lastDigit = year % 10;

        let elementKey;
        if ([0, 1].includes(lastDigit)) elementKey = 'metal';
        else if ([2, 3].includes(lastDigit)) elementKey = 'water';
        else if ([4, 5].includes(lastDigit)) elementKey = 'wood';
        else if ([6, 7].includes(lastDigit)) elementKey = 'fire';
        else elementKey = 'earth';

        const element = this.elements[elementKey];

        // Добавляем персональные рекомендации на основе года
        const personalAdvice = this.getPersonalAdvice(year, elementKey);

        return {
            element: element.name,
            elementKey,
            color: element.color,
            direction: element.direction,
            season: element.season,
            shape: element.shape,
            meaning: element.meaning,
            affirmation: element.affirmation,
            description: element.description,
            activation: element.activation,
            personalAdvice
        };
    }

