    calculate(data) {
        const { question, method, coins, date } = data;

        let hexagramNumber;
        let changingLines = [];

        if (method === 'coins' && coins) {
            // Метод трех монет
            const result = this.castCoins(coins);
            hexagramNumber = result.hexagram;
            changingLines = result.changingLines;
        } else {
            // Случайная гексаграмма для демо
            hexagramNumber = Math.floor(Math.random() * 64) + 1;
            changingLines = [Math.floor(Math.random() * 6) + 1];
        }

        const hexagram = this.hexagrams[hexagramNumber];

        // Если есть изменяющиеся линии, получаем вторичную гексаграмму
        let secondaryHexagram = null;
        if (changingLines.length > 0) {
            const secondaryNumber = this.getSecondaryHexagram(hexagramNumber, changingLines);
            secondaryHexagram = {
                number: secondaryNumber,
                ...this.hexagrams[secondaryNumber]
            };
        }

        return {
            question: question || 'Судьба',
            date: date || new Date().toLocaleDateString(),
            method: method || 'Три монеты',
            primary: {
                number: hexagramNumber,
                name: hexagram.name,
                chinese: hexagram.chinese,
                description: hexagram.description,
                meaning: hexagram.meaning,
                image: hexagram.image,
                changingLines: changingLines,
                lineInterpretations: this.interpretLines(hexagram, changingLines)
            },
            secondary: secondaryHexagram,
            interpretation: this.generateInterpretation(hexagram, changingLines, secondaryHexagram, question)
        };
    }

    castCoins(coinTosses) {
        // coinTosses - массив из 6 бросков, каждый бросок - массив из 3 монет (орел/решка)
        let hexagram = 0;
        const changingLines = [];

        for (let i = 0; i < 6; i++) {
            const toss = coinTosses[i];
            const value = toss.reduce((sum, coin) => sum + (coin === 'heads' ? 3 : 2), 0);

            // Определяем линию
            if (value === 6) { // 2+2+2 - старая инь (меняется)
                changingLines.push(i + 1);
                // В hexagram линия будет 0
            } else if (value === 7) { // 3+2+2 - молодая ян
                hexagram += Math.pow(2, 5 - i);
            } else if (value === 8) { // 3+3+2 - молодая инь
                // линия 0
            } else if (value === 9) { // 3+3+3 - старая ян (меняется)
                changingLines.push(i + 1);
                hexagram += Math.pow(2, 5 - i);
            }
        }

        return { hexagram: hexagram + 1, changingLines };
    }

    getSecondaryHexagram(primaryNumber, changingLines) {
        let primary = primaryNumber - 1;
        let secondary = primary;

        changingLines.forEach(line => {
            const bit = Math.pow(2, 6 - line);
            secondary ^= bit; // XOR - инвертируем бит
        });

        return secondary + 1;
    }

    interpretLines(hexagram, changingLines) {
        if (changingLines.length === 0) return [];

        return changingLines.map(line => ({
            line,
            text: hexagram.lines[line - 1] || `Линия ${line} требует особого внимания.`,
            meaning: `Эта линия изменяется, указывая на важный момент в вашей ситуации.`
        }));
    }

    generateInterpretation(hexagram, changingLines, secondaryHexagram, question) {
        let interpretation = `
🔮 **И-ЦЗИН - КНИГА ПЕРЕМЕН** 🔮

**Ваш вопрос:** "${question || 'Судьба'}"

**Первичная гексаграмма: ${hexagram.number}. ${hexagram.name}** (${hexagram.chinese})

${hexagram.description}

*Значение:* ${hexagram.meaning}

*Образ:* ${hexagram.image}
        `;

        if (changingLines.length > 0) {
            interpretation += `\n\n**Изменяющиеся линии:**\n`;
            changingLines.forEach(line => {
                interpretation += `\nЛиния ${line}: ${hexagram.lines[line-1]}`;
            });
        }

        if (secondaryHexagram) {
            interpretation += `\n\n**Вторичная гексаграмма: ${secondaryHexagram.number}. ${secondaryHexagram.name}** (${secondaryHexagram.chinese})
            
${secondaryHexagram.description}

*Значение:* ${secondaryHexagram.meaning}`;
        }

        interpretation += `\n\n**СОВЕТ И-ЦЗИН**\n\n${hexagram.advice}`;

        if (question) {
            interpretation += `\n\n**ОТВЕТ НА ВАШ ВОПРОС**\n\n`;
            interpretation += this.answerQuestion(hexagram, changingLines, question);
        }

        return interpretation;
    }

