    interpret(numbers) {
        const healthNum = this.calculateHealthNumber(numbers);
        const energyNum = this.calculateEnergyNumber(numbers);

        const base = this.descriptions[healthNum] || this.getDefaultDescription(healthNum);

        return {
            healthNumber: healthNum,
            energyLevel: energyNum,
            title: base.title,
            description: base.description,
            detailedDescription: base.detailedDescription || base.description,
            energyDescription: base.energyLevel || 'Уникальный уровень',
            energyType: base.energyType || '',
            strengths: base.strengths || [],
            weaknesses: base.weaknesses || [],
            vulnerable: base.vulnerable || [],
            recommendations: base.recommendations || [],
            seasonalRisks: base.seasonalRisks || '',
            prevention: base.prevention || [],
            developmentPath: base.developmentPath || base.advice || '',
            successFactors: base.successFactors || [],
            failureFactors: base.failureFactors || [],
            advice: base.advice || '',
            fullInterpretation: this.buildInterpretation(healthNum, energyNum, base)
        };
    }

    calculateHealthNumber(numbers) {
        let healthNum = 10 - (numbers.fate % 9 || 9);
        return healthNum;
    }

    calculateEnergyNumber(numbers) {
        return (numbers.name % 10) + 1;
    }

