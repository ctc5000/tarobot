    interpret(numbers) {
        const moneyNum = this.calculateMoneyNumber(numbers);
        const abundanceNum = this.calculateAbundanceNumber(numbers);

        const base = this.descriptions[moneyNum] || this.getDefaultDescription(moneyNum);

        return {
            moneyNumber: moneyNum,
            abundanceNumber: abundanceNum,
            title: base.title,
            description: base.description,
            moneyStyle: base.moneyStyle,
            strengths: base.strengths,
            weaknesses: base.weaknesses,
            sources: base.sources,
            advice: base.advice,
            abundanceDescription: this.getAbundanceDescription(abundanceNum),
            fullInterpretation: this.buildInterpretation(moneyNum, abundanceNum, base)
        };
    }

    calculateMoneyNumber(numbers) {
        // Финансовое число = число судьбы * 2
        let sum = numbers.fate * 2;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateAbundanceNumber(numbers) {
        // Число изобилия = сумма финансового и карьерного
        const money = this.calculateMoneyNumber(numbers);
        const career = this.calculateCareerNumber(numbers);
        let sum = money + career;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateCareerNumber(numbers) {
        const sum = (numbers.fate + numbers.name) % 9;
        return sum === 0 ? 9 : sum;
    }

