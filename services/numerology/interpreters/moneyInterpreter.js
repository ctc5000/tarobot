    calculateMoneyNumber(numbers) {
        let sum = numbers.fate * 2;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateAbundanceNumber(numbers) {
        const money = this.calculateMoneyNumber(numbers);
        const career = this.calculateCareerNumber(numbers);
        let sum = money + career;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateCareerNumber(numbers) {
        let sum = (numbers.fate + numbers.name) % 9;
        return sum === 0 ? 9 : sum;
    }

