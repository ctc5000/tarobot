    interpret(numbers) {
        const familyNum = this.calculateFamilyNumber(numbers);
        const partnerNum = this.calculatePartnerNumber(numbers);
        const childrenNum = this.calculateChildrenNumber(numbers);

        const base = this.descriptions[familyNum] || this.getDefaultDescription(familyNum);

        return {
            familyNumber: familyNum,
            partnerNumber: partnerNum,
            childrenNumber: childrenNum,
            title: base.title,
            description: base.description,
            role: base.role,
            strengths: base.strengths,
            weaknesses: base.weaknesses,
            advice: base.advice,
            partnerDescription: this.getPartnerDescription(partnerNum),
            childrenDescription: this.getChildrenDescription(childrenNum),
            fullInterpretation: this.buildInterpretation(familyNum, partnerNum, childrenNum, base)
        };
    }

    calculateFamilyNumber(numbers) {
        // Семейное число = число рода + число отчества
        const sum = numbers.surname + numbers.patronymic;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculatePartnerNumber(numbers) {
        // Число партнера = число имени + 6
        const sum = numbers.name + 6;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateChildrenNumber(numbers) {
        // Число детей = число семьи - 3
        const familyNum = this.calculateFamilyNumber(numbers);
        let childrenNum = Math.abs(familyNum - 3);
        if (childrenNum > 9) {
            childrenNum = this.reduceToSingle(childrenNum);
        }
        return childrenNum;
    }

    reduceToSingle(num) {
        while (num > 9) {
            num = String(num).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return num;
    }

