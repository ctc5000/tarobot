    interpret(numbers) {
        const loveNum = this.calculateLoveNumber(numbers);
        const compatibility = this.calculateCompatibility(numbers);

        const base = this.descriptions[loveNum] || this.getDefaultDescription(loveNum);

        return {
            loveNumber: loveNum,
            compatibility: compatibility,
            title: base.title,
            description: base.description,
            loveStyle: base.loveStyle,
            strengths: base.strengths,
            weaknesses: base.weaknesses,
            idealPartner: base.idealPartner,
            advice: base.advice,
            compatibilityLevel: this.getCompatibilityLevel(compatibility),
            fullInterpretation: this.buildInterpretation(loveNum, compatibility, base)
        };
    }

    calculateLoveNumber(numbers) {
        // Число любви = число имени + число судьбы
        const sum = numbers.name + numbers.fate;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateCompatibility(numbers) {
        // Совместимость в процентах
        const love = this.calculateLoveNumber(numbers);
        const family = this.calculateFamilyNumber(numbers);
        // Базовая формула: (любовь + семья) * 5, ограниченная 100
        let compatibility = Math.floor((love + family) * 5.5);
        return Math.min(100, compatibility);
    }

    calculateFamilyNumber(numbers) {
        const sum = numbers.surname + numbers.patronymic;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    getCompatibilityLevel(percent) {
        if (percent >= 90) return '✨ Исключительная совместимость — вы созданы друг для друга';
        if (percent >= 80) return '🌟 Отличная совместимость — гармоничный союз';
        if (percent >= 70) return '💫 Хорошая совместимость — есть над чем работать, но перспективы отличные';
        if (percent >= 60) return '⭐ Средняя совместимость — отношения требуют усилий';
        if (percent >= 50) return '🌙 Ниже средней — нужна серьезная работа над отношениями';
        return '⚡ Сложная совместимость — вызов для обоих';
    }

