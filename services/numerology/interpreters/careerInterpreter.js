    /**
     * Интерпретация карьерного потенциала
     * @param {Object} numbers - базовые числа
     * @returns {Object} интерпретация
     */
    interpret(numbers) {
        const careerNum = this.calculateCareerNumber(numbers);
        const successNum = this.calculateSuccessNumber(numbers);
        const realizationNum = this.calculateRealizationNumber(numbers);

        const base = this.descriptions[careerNum] || this.getDefaultDescription(careerNum);

        return {
            careerNumber: careerNum,
            successNumber: successNum,
            realizationNumber: realizationNum,
            title: base.title,
            description: base.description,
            strengths: base.strengths,
            weaknesses: base.weaknesses,
            suitable: base.suitable,
            advice: base.advice,
            successDescription: this.getSuccessDescription(successNum),
            realizationDescription: this.getRealizationDescription(realizationNum),
            fullInterpretation: this.buildInterpretation(careerNum, successNum, realizationNum, base)
        };
    }

    calculateCareerNumber(numbers) {
        // Сумма числа судьбы и имени
        const sum = (numbers.fate + numbers.name) % 9;
        return sum === 0 ? 9 : sum;
    }

    calculateSuccessNumber(numbers) {
        // Число успеха = сумма всех чисел
        const sum = numbers.fate + numbers.name + numbers.surname + numbers.patronymic;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

    calculateRealizationNumber(numbers) {
        // Число реализации = число судьбы + число рода
        const sum = numbers.fate + numbers.surname;
        while (sum > 9 && sum !== 11 && sum !== 22) {
            sum = String(sum).split('').reduce((s, d) => s + parseInt(d), 0);
        }
        return sum;
    }

