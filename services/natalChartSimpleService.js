    calculate(data) {
        try {
            console.log('Расчет натальной карты для:', data);

            if (!data || !data.birthDate || !data.birthTime) {
                throw new Error('Отсутствуют обязательные данные: дата или время рождения');
            }

            const [day, month, year] = data.birthDate.split('.').map(Number);
            const [hour, minute] = data.birthTime.split(':').map(Number);

            // Валидация даты
            if (isNaN(day) || isNaN(month) || isNaN(year) || isNaN(hour) || isNaN(minute)) {
                throw new Error('Неверный формат даты или времени');
            }

            // Создаем дату в UTC
            const date = new Date(Date.UTC(year, month - 1, day, hour, minute));
            const time = new Astronomy.AstroTime(date);

            // Получаем позиции планет
            const planets = this.getPlanetPositions(time);

            // Рассчитываем асцендент и дома
            const ascendant = this.calculateAscendant(time, data.latitude || 55.7558, data.longitude || 37.6173);
            const houses = this.calculateHouses(ascendant);

            // Формируем обогащенные данные
            const enrichedPlanets = this.enrichPlanetsData(planets);
            const enrichedHouses = this.enrichHousesData(houses, enrichedPlanets);
            const enrichedAspects = this.calculateAndEnrichAspects(enrichedPlanets);

            return {
                success: true,
                data: {
                    fullName: data.fullName || 'Не указано',
                    birthDate: data.birthDate,
                    birthTime: data.birthTime,
                    ascendant: this.getAscendantDescription(ascendant),
                    planets: enrichedPlanets,
                    houses: enrichedHouses,
                    aspects: enrichedAspects,
                    chartData: this.formatForDraw(planets, houses)
                }
            };
        } catch (error) {
            console.error('Ошибка в сервисе:', error);
            return {
                success: false,
                error: error.message
            };
        }
    }

