    calculate(birthDate) {
        const [day, month] = birthDate.split('.').map(Number);
        const date = new Date(2000, month - 1, day);

        // Находим знак зодиака
        let sign = this.signs.find(s => {
            const [startMonth, startDay] = s.start.split('-').map(Number);
            const [endMonth, endDay] = s.end.split('-').map(Number);

            const start = new Date(2000, startMonth - 1, startDay);
            const end = new Date(2000, endMonth - 1, endDay);

            // Особый случай для Козерога (переход через год)
            if (s.name === 'Козерог' && startMonth === 12) {
                const decStart = new Date(2000, 11, 22);
                const janEnd = new Date(2001, 0, 19);
                return (date >= decStart) || (date <= janEnd);
            }

            return date >= start && date <= end;
        });

        // Если не нашли, берем первый (Козерог для января)
        if (!sign) {
            sign = this.signs.find(s => s.name === 'Козерог');
        }

        return {
            name: sign.name,
            element: sign.element,
            quality: sign.quality,
            planet: sign.planet,
            description: sign.description,
            strengths: sign.strengths,
            weaknesses: sign.weaknesses,
            lifeMission: sign.lifeMission,
            deepInsight: sign.deepInsight
        };
    }
